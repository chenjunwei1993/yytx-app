package com.dyibing.myapp.bean;

public class FragmentBean {
    private String fragmentUrl;
    private int userFragmentId;
    private String fragmentId;
    private int fragmentCount;
    private int fragmentSort;

    public String getFragmentUrl() {
        return fragmentUrl;
    }

    public void setFragmentUrl(String fragmentUrl) {
        this.fragmentUrl = fragmentUrl;
    }

    public int getUserFragmentId() {
        return userFragmentId;
    }

    public void setUserFragmentId(int userFragmentId) {
        this.userFragmentId = userFragmentId;
    }

    public String getFragmentId() {
        return fragmentId;
    }

    public void setFragmentId(String fragmentId) {
        this.fragmentId = fragmentId;
    }

    public int getFragmentCount() {
        return fragmentCount;
    }

    public void setFragmentCount(int fragmentCount) {
        this.fragmentCount = fragmentCount;
    }

    public int getFragmentSort() {
        return fragmentSort;
    }

    public void setFragmentSort(int fragmentSort) {
        this.fragmentSort = fragmentSort;
    }
}
